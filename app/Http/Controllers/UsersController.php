    {
        $id = $user["id"];
        $user = User::find($id);
        $user->update([
            'name'=>request()->name,
            'email'=>request()->email,
            'password'=>Hash::make(request()->password),
            'rol'=>request()->rol
        ]);
        return redirect()->route('home', $user)->with(['status'=> "Se editÃ³ el usuario $user->name"]);
